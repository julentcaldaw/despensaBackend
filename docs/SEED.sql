-- Relacion de ingredientes con restricciones (reglas automaticas)
WITH matched AS (
  -- Carne y charcuteria: no apto para dietas vegetarianas/veganas/pescetarianas
  SELECT i.id AS ingredient_id, 'Vegetariana' AS restriction_name
  FROM ingredients i
  JOIN ingredient_categories c ON c.id = i.category_id
  WHERE c.name IN ('Carne', 'Charcutería')

  UNION ALL
  SELECT i.id, 'Vegana'
  FROM ingredients i
  JOIN ingredient_categories c ON c.id = i.category_id
  WHERE c.name IN ('Carne', 'Charcutería')

  UNION ALL
  SELECT i.id, 'Pescetariana'
  FROM ingredients i
  JOIN ingredient_categories c ON c.id = i.category_id
  WHERE c.name IN ('Carne', 'Charcutería')

  -- Cerdo y derivados
  UNION ALL
  SELECT i.id, 'Sin carne de cerdo'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%cerdo%', '%ibéric%', '%cochinillo%', '%jamón%', '%chorizo%', '%salchichón%',
    '%longaniza%', '%sobrasada%', '%morcilla%', '%fuet%', '%pancetta%', '%guanciale%',
    '%mortadela%', '%salami%', '%prosciutto%', '%speck%', '%lomo embuchado%', '%tocino%', '%cotechino%'
  ])

  UNION ALL
  SELECT i.id, 'Halal'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%cerdo%', '%ibéric%', '%cochinillo%', '%jamón%', '%chorizo%', '%salchichón%',
    '%longaniza%', '%sobrasada%', '%morcilla%', '%fuet%', '%pancetta%', '%guanciale%',
    '%mortadela%', '%salami%', '%prosciutto%', '%speck%', '%lomo embuchado%', '%tocino%', '%cotechino%'
  ])

  UNION ALL
  SELECT i.id, 'Kosher'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%cerdo%', '%ibéric%', '%cochinillo%', '%jamón%', '%chorizo%', '%salchichón%',
    '%longaniza%', '%sobrasada%', '%morcilla%', '%fuet%', '%pancetta%', '%guanciale%',
    '%mortadela%', '%salami%', '%prosciutto%', '%speck%', '%lomo embuchado%', '%tocino%', '%cotechino%'
  ])

  -- Pescado y marisco
  UNION ALL
  SELECT i.id, 'Sin pescado'
  FROM ingredients i
  JOIN ingredient_categories c ON c.id = i.category_id
  WHERE c.name = 'Pescado y marisco'

  UNION ALL
  SELECT i.id, 'Sin marisco'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%gamba%', '%camarón%', '%langostino%', '%bogavante%', '%langosta%', '%cigala%', '%centollo%', '%buey de mar%',
    '%nécora%', '%cangrejo%', '%percebe%', '%mejillón%', '%almeja%', '%ostra%', '%vieira%', '%zamburiña%', '%tellina%',
    '%pulpo%', '%sepia%', '%calamar%', '%chipirón%', '%pota%', '%jibia%'
  ])

  UNION ALL
  SELECT i.id, 'Sin moluscos'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%mejillón%', '%almeja%', '%ostra%', '%vieira%', '%zamburiña%', '%tellina%', '%pulpo%', '%sepia%',
    '%calamar%', '%chipirón%', '%pota%', '%jibia%', '%caracol%', '%bígaro%', '%lapa%', '%abulón%', '%cañaílla%'
  ])

  -- Huevos y lacteos
  UNION ALL
  SELECT i.id, 'Vegana'
  FROM ingredients i
  JOIN ingredient_categories c ON c.id = i.category_id
  WHERE c.name = 'Huevos y Lácteos'

  UNION ALL
  SELECT i.id, 'Sin lactosa'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%leche%', '%nata%', '%mantequilla%', '%yogur%', '%kéfir%', '%kefir%', '%queso%', '%ricotta%', '%mozzarella%', '%burrata%',
    '%provolone%', '%asiago%', '%fontina%', '%taleggio%', '%gorgonzola%', '%scamorza%', '%stracchino%', '%brie%', '%camembert%',
    '%roquefort%', '%comté%', '%gruyère%', '%beaufort%', '%reblochon%', '%munster%', '%livarot%', '%cantal%', '%feta%',
    '%halloumi%', '%manouri%', '%labneh%', '%akawi%', '%nabulsi%', '%paneer%', '%fromage%', '%quark%', '%clotted cream%'
  ])

  UNION ALL
  SELECT i.id, 'Sin huevo'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%huevo%', '%clara%', '%yema%', '%ikura%', '%huevas%', '%masago%', '%bottarga%'
  ])

  -- Gluten
  UNION ALL
  SELECT i.id, 'Sin gluten'
  FROM ingredients i
  JOIN ingredient_categories c ON c.id = i.category_id
  WHERE c.name IN ('Panadería', 'Cereales y galletas')
     OR lower(i.name) LIKE ANY (ARRAY[
       '%trigo%', '%cebada%', '%centeno%', '%espelta%', '%kamut%', '%einkorn%', '%bulgur%', '%cuscús%', '%farro%',
       '%pasta%', '%fideos%', '%lasagna%', '%cannelloni%', '%ravioli%', '%tortellini%', '%udon%', '%ramen%', '%soba%',
       '%pan rallado%', '%panko%', '%masa de%', '%wonton%', '%gyoza%'
     ])

  -- Soja
  UNION ALL
  SELECT i.id, 'Sin soja'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%soja%', '%tofu%', '%tempeh%', '%natto%', '%miso%', '%tamari%', '%shoyu%', '%doenjang%', '%ssamjang%', '%edamame%'
  ])

  -- Frutos secos y cacahuetes
  UNION ALL
  SELECT i.id, 'Sin frutos secos'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%almendra%', '%avellana%', '%nuez%', '%pistacho%', '%anacardo%', '%piñón%', '%castaña%', '%macadamia%', '%pecana%', '%bellota%'
  ])

  UNION ALL
  SELECT i.id, 'Sin cacahuetes'
  FROM ingredients i
  WHERE lower(i.name) LIKE '%cacahuete%'

  -- Semillas y condimentos alergeno frecuentes
  UNION ALL
  SELECT i.id, 'Sin sésamo'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY['%sésamo%', '%sesamo%', '%tahini%', '%gomashio%'])

  UNION ALL
  SELECT i.id, 'Sin mostaza'
  FROM ingredients i
  WHERE lower(i.name) LIKE '%mostaza%'

  UNION ALL
  SELECT i.id, 'Sin apio'
  FROM ingredients i
  WHERE lower(i.name) LIKE '%apio%'

  UNION ALL
  SELECT i.id, 'Sin altramuces'
  FROM ingredients i
  WHERE lower(i.name) LIKE '%altramuc%'

  -- Sulfitos (aproximacion por bebidas/vinagres/encurtidos)
  UNION ALL
  SELECT i.id, 'Sin sulfitos'
  FROM ingredients i
  WHERE lower(i.name) LIKE ANY (ARRAY[
    '%vino%', '%jerez%', '%oporto%', '%cava%', '%champagne%', '%sidra%', '%cerveza%', '%vermut%', '%vinagre%'
  ])
)
