INSERT INTO recipe_ingredients ("recipeId", "ingredientId", quantity, unit)
SELECT r.id, i.id, p.quantity, p.unit
FROM pairs p
JOIN recipes r ON r.name = p.recipe_name
JOIN ingredients i ON i.name = p.ingredient_name
ON CONFLICT ("recipeId", "ingredientId") DO UPDATE
SET quantity = EXCLUDED.quantity,
    unit = EXCLUDED.unit;
